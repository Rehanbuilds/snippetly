export interface EmailValidationResult {
  isValid: boolean
  error?: string
}

export function validateEmail(email: string): EmailValidationResult {
  // Basic format validation
  const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/
  if (!emailRegex.test(email)) {
    return {
      isValid: false,
      error: "Please enter a valid email address",
    }
  }

  // Extract domain
  const domain = email.toLowerCase().split("@")[1]

  if (!domain) {
    return {
      isValid: false,
      error: "Invalid email format",
    }
  }

  // Check if it's a disposable email domain
  if (DISPOSABLE_DOMAINS.includes(domain)) {
    return {
      isValid: false,
      error:
        "Disposable email addresses are not allowed. Please use a valid email address from Gmail, Outlook, Yahoo, or your organization.",
    }
  }

  // Check for common patterns in disposable emails
  const suspiciousPatterns = [
    /temp/i,
    /trash/i,
    /throw/i,
    /fake/i,
    /spam/i,
    /disposable/i,
    /guerrilla/i,
    /mailinator/i,
    /10minute/i,
    /burner/i,
  ]

  const hasSuspiciousPattern = suspiciousPatterns.some((pattern) => pattern.test(domain))
  if (hasSuspiciousPattern && !TRUSTED_PROVIDERS.includes(domain)) {
    return {
      isValid: false,
      error:
        "This email provider is not allowed. Please use a valid email address from Gmail, Outlook, Yahoo, or your organization.",
    }
  }

  // Additional validation: check for very short domains (often disposable)
  const domainParts = domain.split(".")
  const tld = domainParts[domainParts.length - 1]

  if (tld.length < 2 || domainParts.length < 2) {
    return {
      isValid: false,
      error: "Invalid email domain",
    }
  }

  return {
    isValid: true,
  }
}

export function isDisposableEmail(email: string): boolean {
  const domain = email.toLowerCase().split("@")[1]
  return DISPOSABLE_DOMAINS.includes(domain)
}

export function isTrustedProvider(email: string): boolean {
  const domain = email.toLowerCase().split("@")[1]
  return TRUSTED_PROVIDERS.includes(domain)
}
